var grade = 'd';

switch (grade) {
    case 'a':
        console.log('good job')
        break;
    case 'b':
        console.log('Pretty good')
        break;
    case 'c':
        console.log('Passed')
        break;
    case 'd':
        console.log('Not so good')
        break;
    case 'f':
        console.log('Failed')

    default:
    console.log('unknown grade')
}